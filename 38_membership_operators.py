# membership operators

text = "Hello, World!"
print("H" in text)
print("hello" not in text)

numbers = [1, 2, 3, 4, 5]
print(2 in numbers)
print(6 not in numbers)

tup = (10, 20, 30)
print(20 in tup)
print(25 not in tup)

fruits = {"apple", "banana", "cherry"}
print("banana" in fruits)
print("grape" not in fruits)

grades = {"Alice": "A", "Bob": "B"}
print("Alice" in grades)
print("A" not in grades)
